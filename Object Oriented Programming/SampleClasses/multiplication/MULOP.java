package SampleClasses.multiplication;

public class MULOP {
    public void infomulop() {
        System.out.println("This is MULOP");
    }

    public void twowaymultiplication(int integer1, int integer2) {
        System.out.println("The result of " + integer1 + " * " + integer2 + " is " + (integer1 * integer2));
    }

    public void threewaymultiplication(int integer1, int integer2, int integer3) {
        System.out.println("The result of " + integer1 + " * " + integer2 + " * " + integer3 + " is "
                + (integer1 * integer2 * integer3));
    }
}